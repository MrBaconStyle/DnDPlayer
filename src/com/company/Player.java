package com.company;

public class Player {
    private String name;
    private int str;
    private int dex;
    private int cons;
    private int intel;
    private int wisd;
    private int cha;

    public Player() {
        this.name = name;
        this.str = str;
        this.dex = dex;
        this.cons = cons;
        this.intel = intel;
        this.wisd = wisd;
        this.cha = cha;
    }

    public String getName() {
        return name;
    }

    public int getStr() {
        return str;
    }

    public int getDex() {
        return dex;
    }

    public int getCons() {
        return cons;
    }

    public int getIntel() {
        return intel;
    }

    public int getWisd() {
        return wisd;
    }

    public int getCha() {
        return cha;
    }
}
